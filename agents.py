class TimeoutException(Exception):
    pass

class AlphaBetaAgent:
    def __init__(self, depth_limit=4, time_limit=3.0):
        self.depth_limit = depth_limit
        self.time_limit = time_limit
        self.start_time = 0
        self.nodes_expanded = 0

    def get_move(self, game, player):
        """This acts as the ALPHA-BETA-SEARCH function from the slide."""
        self.start_time = time.time()

        try:
            utility, move = self.max_value(game, self.depth_limit, -float('inf'), float('inf'), player)
            if move is not None:
                return move
            else:
                return random.choice(game.get_legal_moves())
        except TimeoutException:
            legal_moves = game.get_legal_moves()
            return random.choice(legal_moves) if legal_moves else None

    def max_value(self, game, depth, alpha, beta, player):
        """Returns a (utility, move) pair for the maximizing player."""
        self.check_time()
        self.nodes_expanded += 1

        opponent = 'O' if player == 'X' else 'X'

        if game.check_winner(player): return (10000 + depth, None)
        if game.check_winner(opponent): return (-10000 - depth, None)
        if not game.get_legal_moves(): return (0, None)
        if depth == 0: return (self.evaluate(game, player), None)

        v = -float('inf')
        best_move = None

        for move in game.get_legal_moves():
            row, col = move
            game.board[row][col] = player

            v2, _ = self.min_value(game, depth - 1, alpha, beta, player)
            game.board[row][col] = game.empty_cell

            if v2 > v:
                v = v2
                best_move = move
                alpha = max(alpha, v)
            if v >= beta:
                return v, best_move

        return v, best_move

    def min_value(self, game, depth, alpha, beta, player):
        """Returns a (utility, move) pair for the minimizing opponent."""
        self.check_time()
        self.nodes_expanded += 1

        opponent = 'O' if player == 'X' else 'X'

        if game.check_winner(player): return (10000 + depth, None)
        if game.check_winner(opponent): return (-10000 - depth, None)
        if not game.get_legal_moves(): return (0, None)
        if depth == 0: return (self.evaluate(game, player), None)

        v = float('inf')
        best_move = None

        for move in game.get_legal_moves():
            row, col = move
            game.board[row][col] = opponent

            v2, _ = self.max_value(game, depth - 1, alpha, beta, player)
            game.board[row][col] = game.empty_cell

            if v2 < v:
                v = v2
                best_move = move
                beta = min(beta, v)
            if v <= alpha:
                return v, best_move

        return v, best_move

    def check_time(self):
        """Throws an error if we are dangerously close to the 3-second limit."""
        if time.time() - self.start_time > (self.time_limit - 0.1):
            raise TimeoutException()

    # ==========================================
    # EVALUATION FUNCTION & HELPERS
    # ==========================================
    def evaluate(self, game, player):
        score = 0
        opponent = 'O' if player == 'X' else 'X'

        # 1. Evaluate Center Control.
        center_weights = [
            [1, 1, 1, 1, 1],
            [1, 5, 5, 5, 1],
            [1, 5, 10, 5, 1],
            [1, 5, 5, 5, 1],
            [1, 1, 1, 1, 1]
        ]
        for r in range(5):
            for c in range(5):
                if game.board[r][c] == player:
                    score += center_weights[r][c]
                elif game.board[r][c] == opponent:
                    score -= 2 * center_weights[r][c]

        # 2 & 3. Evaluate Threats and Blocking
        windows = self.get_all_windows_of_4(game)

        for window in windows:
            score += self.score_window(window, player, opponent, game.empty_cell)

        return score

    # FIXED: Moved out of evaluate() to be its own peer method
    def get_all_windows_of_4(self, game):
        """Extracts all possible 4-cell lines (windows) from the 5x5 board."""
        windows = []
        board = game.board

        # 1. Horizontal windows
        for r in range(5):
            for c in range(2):
                current_window = []
                for i in range(4):
                    current_window.append(board[r][c+i])
                # FIXED: Outdented
                windows.append(current_window)

        # 2. Vertical windows
        for c in range(5):
            for r in range(2):
                current_window = []
                for i in range(4):
                    current_window.append(board[r+i][c])
                # FIXED: Outdented
                windows.append(current_window)

        # 3. Diagonals
        for r in range(2):
            for c in range(2):
                current_window = []
                for i in range(4):
                    current_window.append(board[r+i][c+i])
                # FIXED: Outdented
                windows.append(current_window)

        # 4. Anti-diagonals
        for r in range(3, 5):
            for c in range(2):
                current_window = []
                for i in range(4):
                    current_window.append(board[r-i][c+i])
                # FIXED: Outdented
                windows.append(current_window)

        return windows

    # FIXED: Added empty_cell argument so it knows what to look for
    def score_window(self, window, player, opponent, empty_cell):
        player_count = window.count(player)
        opp_count = window.count(opponent)
        empty_count = window.count(empty_cell)

        # Give points for your open threats
        if player_count == 3 and empty_count == 1:
            return 100
        elif player_count == 2 and empty_count == 2:
            return 10

        # Subtract heavily for opponent threats
        if opp_count == 3 and empty_count == 1:
            return -200
        if opp_count == 2 and empty_count == 2:
            return -20

        # Dead lines, no points (FIXED: added colon)
        else:
            return 0

