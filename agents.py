class QLearningAgent:
  def __init__(self, alpha=0.1, gamma=0.95, use_symmetry=True):
    self.alpha = alpha
    self.gamma = gamma
    self.use_symmetry = use_symmetry
    self.q_table = {} # Use a Sparse Q-table
    #Layout: self.q_table[state_tuple][action_tuple] = q_value

  def _transform_state(self, state, config_idx):
    """Apply one of the 8 dihedral group (D4)
    symmetry transformations to a flat 25 board tuple representation"""
# Use a Sparse Q-table
    grid = [state[i:i+5] for i in range(0, 25, 5)]

    if config_idx == 0:
      return state
    elif config_idx == 1: # Rotation 90° Clockwise
      return tuple(grid[4-c][r] for r in range(5) for c in range(5))
    elif config_idx == 2: # Rotation 180°
      return tuple(grid[4-r][4-c] for r in range(5) for c in range(5))
    elif config_idx == 3: # Rotation 270°
      return tuple(grid[c][4-r] for r in range(5) for c in range(5))
    elif config_idx == 4: # Horizontal Flip
      return tuple(grid[r][4-c] for r in range(5) for c in range(5))
    elif config_idx == 5: # Vertical Flip
      return tuple(grid[4-r][c] for r in range(5) for c in range(5))
    elif config_idx == 6: # Transpose Diagonal
      return tuple(grid[c][r] for r in range(5) for c in range(5))
    elif config_idx == 7: # Anti-Transpose Diagonal
      return tuple(grid[4-c][4-r] for r in range(5) for c in range(5))


  def _to_canonical(self, state, action):
        """
        Transforms a state-action pair into its unique canonical representation
        by finding the lexicographically smallest state tuple variant.
        """
        if not self.use_symmetry:
            return state, action

        r, c = action
        # Map how the coordinate (r, c) alters under each of the 8 grid transforms
        trans_actions = [
            (r, c), (c, 4-r), (4-r, 4-c), (4-c, r),
            (r, 4-c), (4-r, c), (c, r), (4-c, 4-r)
        ]

        canonical_state = None
        canonical_action = None

        for i in range(8):
            t_state = self._transform_state(state, i)
            if canonical_state is None or t_state < canonical_state:
                canonical_state = t_state
                canonical_action = trans_actions[i]

        return canonical_state, canonical_action

  def get_q(self, state, action):
      """Safely fetches a Q-value, defaulting unseen values to 0 (Section 3.3 & 4.1)."""
      c_state, c_action = self._to_canonical(state, action)
      if c_state not in self.q_table:
          return 0.0
      return self.q_table[c_state].get(c_action, 0.0)

  def set_q(self, state, action, value):
      """Assigns a calculated Q-value to the sparse canonical state key."""
      c_state, c_action = self._to_canonical(state, action)
      if c_state not in self.q_table:
          self.q_table[c_state] = {}
      self.q_table[c_state][c_action] = value

  def _get_legal_actions_from_state(self, state):
      """Helper to extract legal coordinate moves from an active perspective state."""
      legal = []
      for idx, val in enumerate(state):
          if val == 0:  # 0 denotes an empty cell
              legal.append((idx // 5, idx % 5))
      return legal

  # --- 1. Epsilon-Greedy Action Selection (Section 4.1 Requirement) ---
  def select_action(self, state, legal_actions, epsilon):
      """
      Selects an action using an epsilon-greedy policy over legal actions.
      Set epsilon = 0 manually during evaluation modes (Section 3.3).
      """
      if not legal_actions:
          return None

      # Exploration phase
      if random.random() < epsilon:
          return random.choice(legal_actions)

      # Exploitation phase (Greedy Q choice)
      q_values = [self.get_q(state, a) for a in legal_actions]
      max_q = max(q_values)

      # Section 4.1: Break ties uniformly at random
      best_actions = [a for a, q in zip(legal_actions, q_values) if q == max_q]
      return random.choice(best_actions)

  # --- 2. Temporal Difference Q-Learning Update (Section 3.3 Requirement) ---
  def update(self, state, action, reward, next_state, done):
      """
      Updates the Q-table entry following the exact Bellman optimality equation.
      Ensures strict compliance with Section 4.4 constraints.
      """
      current_q = self.get_q(state, action)

      if done:
          # Section 4.4 Mistake Guard: Target is only the reward if terminal
          target = reward
      else:
          # Section 4.4 Mistake Guard: Max over LEGAL actions only
          next_legal = self._get_legal_actions_from_state(next_state)
          if next_legal:
              max_next_q = max([self.get_q(next_state, a_prime) for a_prime in next_legal])
          else:
              max_next_q = 0.0

          # Section 3.3: Target calculation formula
          target = reward + self.gamma * max_next_q

      # Standard TD update equation: Q(s,a) = Q(s,a) + alpha * (target - Q(s,a))
      new_q = current_q + self.alpha * (target - current_q)
      self.set_q(state, action, new_q)

  # --- 3. Save/Load Model Artifacts (Section 4.1 Requirement) ---
  def save_q_table(self, filepath):
      """Saves the sparse dictionary to disk using Pickle serialization."""
      with open(filepath, 'wb') as f:
          pickle.dump(self.q_table, f)
      print(f"Successfully saved Q-table containing {len(self.q_table)} state keys.")

  def load_q_table(self, filepath):
      """Loads a pre-trained sparse dictionary from disk."""
      with open(filepath, 'rb') as f:
          self.q_table = pickle.load(f)
      print(f"Successfully loaded Q-table containing {len(self.q_table)} state keys.")

